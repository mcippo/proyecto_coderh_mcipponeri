CREATE TABLE IF NOT EXISTS recorridos (
	id_recorrido INT PRIMARY KEY NOT NULL,
	id_usuario INT NOT NULL,
	id_estacion_orig INT NOT NULL,
  id_mes INT NOT NULL,
  fecha_origen DATETIME,
  id_estacion_dest INT NOT NULL,
  fecha_dest DATETIME,
  id_modelo INT NOT NULL,
