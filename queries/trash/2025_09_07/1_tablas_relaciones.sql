    FOREIGN KEY (id_estacion_orig) REFERENCES estaciones(id_estacion),
    FOREIGN KEY (id_mes) REFERENCES meses(id_mes),
    FOREIGN KEY (id_estacion_dest) REFERENCES estaciones(id_estacion),
